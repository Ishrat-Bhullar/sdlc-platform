"""
agents/database/agent.py
==========================
Database Agent — the single, production Database Agent implementation for
the platform (consolidated from two prior implementations: this richer,
previously-unwired agents/db_agent.py, and a simpler inline implementation
in ai_service.py that was actually wired into the pipeline). Owns its own
prompts (prompts.py) and schema (schemas.py); the pipeline orchestrator
only ever calls `.generate(...)`.

Consolidation notes (why this file looks the way it does):
  - `generate_schema_and_migrations(ba_output, db_type)` and
    `chat_with_schema(schema_json, user_query)` are DatabaseAgent's
    original, richer, typed methods — unchanged.
  - `.generate(db, project_id, context)` is the NEW orchestrator-facing
    entrypoint that replaces ai_service.generate_database_schema. It
    adapts the pipeline's flat `context` string into a BusinessAnalystOutput
    -shaped input by fetching the project's actual `user_stories`
    GeneratedArtifact (falling back to wrapping the flat context if that
    artifact isn't available yet), then calls
    generate_schema_and_migrations — preserving the existing artifact
    contract (SQL_SCHEMA content as embedded JSON text) so downstream
    agents (UI/UX, Frontend, Backend, Documentation) need no changes.
"""
