def build_schema_prompt(
    ba_output: BusinessAnalystOutput | dict,
    db_type: str
) -> str:
    """
    Assembles user prompt instruction payloads matching targeted database options.
    """
    payload = ba_output.model_dump() if hasattr(ba_output, "model_dump") else ba_output

    return f"""
Target Database Type: {db_type}

Business Analyst Output:
{json.dumps(payload, indent=2)}

Generate:
1. Entities
2. Columns
3. Primary Keys
4. Unique Constraints
5. Indexes
6. Relationships
7. Migration Scripts (UP and DOWN)
8. Full SQL DDL
9. Scaling strategy and partitioning recommendations
10. Design decisions, normalization notes, ER diagram, audit tables, sample data

Return valid JSON only.
"""


def build_schema_chat_prompt(
    schema_json: DatabaseSchemaOutput | dict,
    user_query: str
) -> str:
    """
    Formats contextual grounding data maps for relational design review discussions.
    """
    payload = schema_json.model_dump() if hasattr(schema_json, "model_dump") else schema_json

    return f"""
Database Schema:
{json.dumps(payload, indent=2)}

User Query:
{user_query}

Answer based only on the schema.
Return JSON:
{{
    "answer": "string",
    "referenced_tables": ["table1"]
}}
"""


class DatabaseAgent:
    """
    Orchestrates table definition generations, DDL compilation runs,
    and granular analysis dialogues using validated runtime schemas.
    """

    def __init__(self, llm: LLMService | None = None, *, db=None, project_id: int | None = None):
        self.llm = llm or LLMService(db=db, project_id=project_id, role="database")

    def generate_schema_and_migrations(
        self,
        ba_output: BusinessAnalystOutput | dict,
        db_type: str = "PostgreSQL"
    ) -> DatabaseSchemaOutput:
        """
        Translates upstream business user contexts directly into a database layout.
        """

        if not ba_output:
            raise ValueError("Upstream inputs and context components parameters cannot be null or empty.")

        if db_type not in SUPPORTED_DB_TYPES:
            raise ValueError(
                f"Requested database dialect translation targets unsupported by platform runtime options: {db_type}"
            )

        result = self.llm.generate_json(
            system=DATABASE_SYSTEM_PROMPT,
            prompt=build_schema_prompt(
                ba_output=ba_output,
                db_type=db_type
            ),
            schema=DatabaseSchemaOutput,
        )

        if not isinstance(result, DatabaseSchemaOutput):
            raise ValueError("Operational parsing exception encountered: Result failed signature validations checks.")

        if not result.entities:
            raise ValueError("Runtime setup verification warnings: Engine produced zero target data tables.")

        return result

    def chat_with_schema(
        self,
        schema_json: DatabaseSchemaOutput | dict,
        user_query: str
    ) -> SchemaChatOutput:
        """
        Executes granular search validations over historical architecture profiles.
        """

        if not schema_json:
            raise ValueError("Target database architecture configurations contexts cannot be empty.")

        if not user_query.strip():
            raise ValueError("Input conversation user diagnostic string fields cannot be empty or blank.")

        result = self.llm.generate_json(
            system=DATABASE_CHAT_PROMPT,
            prompt=build_schema_chat_prompt(
                schema_json=schema_json,
                user_query=user_query
            ),
            schema=SchemaChatOutput,
        )

        if not isinstance(result, SchemaChatOutput):
            raise ValueError("Operational parsing exception encountered: Result failed chat output signature checks.")

        return result

    @classmethod
    def generate(cls, db, project_id: int, context: str, db_type: str = "PostgreSQL") -> dict[str, Any]:
        """Orchestrator-facing entrypoint: `DatabaseAgent.generate(db, project_id, context)`.
        Adapts the pipeline's flat context string into a BusinessAnalystOutput
        -shaped input (fetching the project's real user_stories artifact when
        available, so the schema is grounded in actual epics/stories rather
        than just prose) and preserves the DEMO_MODE / AIGenerationError
        contract of the previous inline ai_service.generate_database_schema."""
        from ...models import DEMO_MODE, ArtifactType, GeneratedArtifact
        from ...ai_service import AIGenerationError

        if DEMO_MODE:
            return MOCK_DATABASE_SCHEMA

        ba_output: dict[str, Any]
        user_stories_artifact = (
            db.query(GeneratedArtifact)
            .filter(
                GeneratedArtifact.project_id == project_id,
                GeneratedArtifact.artifact_type == ArtifactType.USER_STORIES.value,
            )
            .order_by(GeneratedArtifact.created_at.desc())
            .first()
        )
        if user_stories_artifact is not None:
            try:
                ba_output = json.loads(user_stories_artifact.content)
            except Exception:
                ba_output = {"context": context}
        else:
            ba_output = {"context": context}

        try:
            llm = LLMService(db=db, project_id=project_id, role="database", timeout=170)
            agent = cls(llm=llm)
            result = agent.generate_schema_and_migrations(ba_output=ba_output, db_type=db_type)
            return result.model_dump()
        except Exception as exc:
            logger.error("[DatabaseAgent] generate failed: %s", exc)
            raise AIGenerationError(f"Database schema generation failed: {exc}") from exc
